// Helper function to get partner universities for a specific Cyprus university
export const getPartnerUniversitiesForHome = (
  homeUniversityName: string,
): PartnerUniversity[] => {
  return PARTNER_UNIVERSITIES.filter((partner) =>
    partner.agreements.includes(homeUniversityName),
  );
};

// Helper function to get all unique partner universities
export const getAllPartnerUniversities = (): PartnerUniversity[] => {
  return PARTNER_UNIVERSITIES;
};

// Helper function to format university display name
export const formatUniversityDisplay = (partner: PartnerUniversity): string => {
  return `${partner.name} - ${partner.city}, ${partner.country}`;
};
